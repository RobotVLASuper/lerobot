"""Action Chunking Transformer Policy

As per Learning Fine-Grained Bimanual Manipulation with Low-Cost Hardware (https://arxiv.org/abs/2304.13705).
The majority of changes here involve removing unused code, unifying naming, and adding helpful comments.
"""

from collections import deque

import torch
from torch import Tensor

from lerobot.common.policies.pretrained import PreTrainedPolicy


class RemotePolicy(PreTrainedPolicy):
    config_class = RemoteConfig
    name = "remote"

    def __init__(
        self,
        config: RemoteConfig,
        dataset_stats: dict[str, dict[str, Tensor]] | None = None,
    ):
        """
        Args:
            config: Policy configuration class instance or None, in which case the default instantiation of
                    the configuration class is used.
            dataset_stats: Dataset statistics to be used for normalization. If not passed here, it is expected
                that they will be passed with a call to `load_state_dict` before the policy is used.
        """
        super().__init__(config)

        self.grpc_client = grpc.insecure_channel(self.config.grpc_server_address)
        self.stub = RemotePolicyStub(self.grpc_client)

        """This should be called whenever the environment is reset."""
        if self.config.temporal_ensemble_coeff is not None:
            self.temporal_ensembler.reset()
        else:
            self._action_queue = deque([], maxlen=self.config.n_action_steps)

    @torch.no_grad
    def select_action(self, batch: dict[str, Tensor]) -> Tensor:
        self.stub.SelectAction(SelectActionRequest(batch=batch))

    def forward(self, batch: dict[str, Tensor]) -> tuple[Tensor, dict]:
        raise NotImplementedError("Remote policy does not support forward pass.")
